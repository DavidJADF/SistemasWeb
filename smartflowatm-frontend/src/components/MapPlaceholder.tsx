// Carga dinámica para no romper SSR
const MapContainer = dynamic(
  () => import('react-leaflet').then((mod) => mod.MapContainer),
  { ssr: false }
);
const TileLayer = dynamic(
  () => import('react-leaflet').then((mod) => mod.TileLayer),
  { ssr: false }
);
const Marker = dynamic(
  () => import('react-leaflet').then((mod) => mod.Marker),
  { ssr: false }
);
const Popup = dynamic(
  () => import('react-leaflet').then((mod) => mod.Popup),
  { ssr: false }
);

interface MapProps { lat: number; lng: number; }
export default function MapPlaceholder({ lat, lng }: MapProps) {
  return (
    <div className="h-64 w-full rounded-lg overflow-hidden shadow">
      <MapContainer center={[lat, lng]} zoom={13} className="h-full w-full">
        <TileLayer url="https://{s}.tile.openstreetmap.org/{z}/{x}/{y}.png" />
        <Marker position={[lat, lng]}>
          <Popup>Ubicación del cajero</Popup>
        </Marker>
      </MapContainer>
    </div>
  );
}
